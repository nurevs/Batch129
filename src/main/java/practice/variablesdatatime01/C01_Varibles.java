package practice.variablesdatatime01;

public class C01_Varibles {
    public static void main(String[] args) {

        //byte,short,long,integer,double,float data typelarinin
        // max ve min degerlerini yazdiran kodu yaziniz


        byte byteMax= Byte.MAX_VALUE;
        byte byteMin= Byte.MIN_VALUE;
        System.out.println("byteMax= "+byteMax);
        System.out.println("byteMin= "+byteMin);

        short shortMax= Short.MAX_VALUE;
        short shortMin= Short.MIN_VALUE;
        System.out.println("shortMin= "+shortMax);
        System.out.println("shortMax= "+shortMin);

        long longMax= Long.MAX_VALUE;
        long longMin= Long.MIN_VALUE;
        System.out.println("longMax= "+longMax);
        System.out.println("longMin= "+longMin);

        int intMax= Integer.MAX_VALUE;
        int intMin= Integer.MIN_VALUE;
        System.out.println("intMax= "+intMax);
        System.out.println("intMin= "+intMin);

        double doubleMax= Double.MAX_VALUE;
        double doubleMin=Double.MIN_VALUE;
        System.out.println("doubleMax= "+doubleMax);
        System.out.println("doubleMin= "+doubleMin);

        float floatMax= Float.MAX_VALUE;
        float floatMin= Float.MIN_VALUE;
        System.out.println("floatMax= "+floatMax);
        System.out.println("floatMin= "+floatMin);









    }
}
